package com.xworkz.controlstatements;

public class RTO {

	public static void main(String[] args) {
		int rtoNumber=300;
		
		switch(rtoNumber)
	   {
		
	     case 45: System.out.println("Hunsur"); break;
		
	     case 9: System.out.println("Mysore, Ashokpuram"); break;
	     
	     case 10: System.out.println("Chamrajnagar"); break;
	     
	     case 1: System.out.println("Bangalore"); break;
	     
	     default: System.out.println("Wrong RTO number");
		
		}

	}

}
